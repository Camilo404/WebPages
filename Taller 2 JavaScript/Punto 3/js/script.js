function cambiocolor() {
    let colores = document.getElementsByClassName("azulblanco");

    for (let i = 0; i < colores.length; i++) {
        colores[i].style.backgroundColor = "#000000";
    }

    let rojo = document.getElementById("exprojo");
    rojo.style.backgroundColor = "#000000";
    rojo.style.color = "#FF0000";
}

